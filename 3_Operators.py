# Lesson: The Operators
name = "Lewence"  # Assignment operator (=) - assign a value to a variable
print(name)

# Floor Division (//) - round down to the nearest integer
print("Floor Division: ", 24 // 5)

# Round Up
print("Round Up: ", round(24 / 5))

# Remainder (%)
print("Remainder: ", 24 % 5)

# Exponents (**)
print("Exponents: ", 2**3)

# not - used for boolean
print("not: ", not True)

# If-else statement
if 15 > 10:
    print("It is greater")
else:
    print("It is less")

# Ternary Operators
print("It is greater") if 15 > 10 else print("It is less")
